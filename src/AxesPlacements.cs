namespace RD_AAOW
	{
	/// <summary>
	/// Возможные расположения осей
	/// </summary>
	public enum AxesPlacements
		{
		/// <summary>
		/// Автоматическое (в точке 0 или со стороны нуля другой оси)
		/// </summary>
		Auto = 0,

		/// <summary>
		/// Слева или сверху
		/// </summary>
		LeftTop = 1,

		/// <summary>
		/// Справа или снизу
		/// </summary>
		RightBottom = 2,

		/// <summary>
		/// По центру или посередине
		/// </summary>
		MiddleCenter = 3
		}
	}
